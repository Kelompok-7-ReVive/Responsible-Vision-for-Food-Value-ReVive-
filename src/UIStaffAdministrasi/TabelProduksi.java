    private void setelahInisialisasi() {
        setLocationRelativeTo(null);
        setTitle("Tabel Produksi - Wilayah " + penggunaSaatIni.getWilayahDikelola());
        jLabel2.setText(penggunaSaatIni.getNama() + " (Staf Administrasi)"); // Personalisasi subjudul

        // Atur JTable
        modelTabel = new DefaultTableModel(
            new Object[][]{},
            new String[]{
                "ID PRODUKSI", "ID HOTEL", "KATEGORI PRODUKSI", "TOTAL PRODUKSI (KG)", "TANGGAL PRODUKSI"
            }
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Membuat semua sel tidak bisa diedit
            }
        };
        TabelProduksi.setModel(modelTabel);
        TabelProduksi.getTableHeader().setReorderingAllowed(false);
        
        // Muat data untuk pertama kali
        muatUlangTabel();
    }
    
    /**
     * Mengambil data dari service dan mengisi tabel.
     */
    private void muatUlangTabel() {
        try {
            modelTabel.setRowCount(0); // Kosongkan tabel sebelum diisi

            // Panggil service untuk mengambil data produksi berdasarkan ID staf
            List<LayananStaf.BarisProduksiUntukTabel> daftarBaris = layananStaf.ambilDataProduksiUntukStaf(penggunaSaatIni.getIdUser());

            // Isi tabel baris per baris
            for (LayananStaf.BarisProduksiUntukTabel baris : daftarBaris) {
                modelTabel.addRow(new Object[]{
                    baris.idProduksi(),
                    baris.idHotel(),
                    baris.kategori(),
                    baris.totalProduksi(),
                    baris.tanggalProduksi()
                });
            }
        } catch (Exception ex) {
            logger.log(Level.SEVERE, "Gagal memuat data produksi", ex);
            JOptionPane.showMessageDialog(this, "Gagal memuat data produksi: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

